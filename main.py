#iterating range with for loop
for i in range(6):
  print(i) #print 0 to 5
 
 #explicitly convert range to list
my_range = range(5)
print(list(my_range)) # [0,1,2,3,4]

#start and stop arguments
for i in range(4,10):
  print(i) #print 4 to 9

#step argument
#print all even numbers from 2 to 10
for i in range(2,10,2):
  print(str(i) + " is even")





