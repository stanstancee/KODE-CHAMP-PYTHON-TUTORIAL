#Addition Operator (+)
print(6 + 9) #outputs 15

#Subtraction Operator (-) 
print(9 - 6) #outputs 3

#Multiplication Operator (*)
print(9 * 6) #outputs 54

#Division Operator (/)
print(9 / 6) #outputs 1.5

#Modulus Operator (%)
print(9 % 6) #outputs 3

#Exponent Operator (**)
print(2 ** 3) #outputs 8

#Floor Division Operator (//)
print(9 // 6) #outputs 1