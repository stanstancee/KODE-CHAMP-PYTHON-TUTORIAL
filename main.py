
 
a , b = 12, 6
# Print bitwise AND operation
print("a & b =", a & b) #prints a & b = 4
 
# Print bitwise OR operation
print("a | b =", a | b) #prints a | b = 14
 
# Print bitwise NOT operation
print("~a =", ~a) #prints ~a = -13

# print bitwise XOR operation
print("a ^ b =", a ^ b) #prints a ^ b = 10

a, b = 20, -20 
# print bitwise right shift operator
print("a >> 1 =", a >> 1) #prints a >> 1 = 10
print("b >> 1 =", b >> 1) #prints b >> 1 = -10
 
a, b = 10, -20
# print bitwise left shift operator
print("a << 1 =", a << 1) #prints a << 1 = 20
print("b << 1 =", b << 1) #prints b << 1 = -20